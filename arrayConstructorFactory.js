// Constructor Pattern
const arr1 = new Array() //[]
console.log(arr1, arr1.length)

const a2 = new Array(5) //we can mention array length here
console.log(a2, a2.length)

const a3 = new Array(1,2,3,4,5) //here we can input array items but can't mention array length
console.log(a3, a3.length)


